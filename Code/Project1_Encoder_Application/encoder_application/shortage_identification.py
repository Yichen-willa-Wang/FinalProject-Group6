def predict_new_data(model_path, tokenizer_path, batch_size=16, device=None):
    # Set device (default to CUDA if available)
    device = device or ("cuda" if torch.cuda.is_available() else "cpu")
    print(f"Using device: {device}")

    # Load tokenizer and model
    tokenizer = AutoTokenizer.from_pretrained(tokenizer_path)
    model = AutoModelForSequenceClassification.from_pretrained(model_path)
    model.to(device)  # Move model to the device
    model.eval()

    # Load data
    comment = pd.read_csv("../df_comments.csv")
    sub = pd.read_csv("../df_submissions.csv")

    # Normalize text
    comment["text1"] = comment["text"].astype(str).str.lower()
    sub["text1"] = sub["text"].astype(str).str.lower()

    # Merge data
    merged_df = pd.concat([comment, sub], ignore_index=True)
    texts = merged_df["text1"].dropna().tolist()

    predictions = []
    for i in tqdm(range(0, len(texts), batch_size), desc="Predicting Batches"):
        batch_texts = texts[i:i+batch_size]
        encodings = tokenizer(batch_texts, padding=True, truncation=True, max_length=512, return_tensors="pt").to(device)

        with torch.no_grad():
            outputs = model(**encodings)
            batch_predictions = torch.argmax(outputs.logits, axis=-1).cpu().numpy()  # Move back to CPU for numpy
            predictions.extend(batch_predictions)

    merged_df["predicted_label"] = predictions
    merged_df.to_csv(drive_save_path, index=False)
    print(f"✅ Predictions saved to: {drive_save_path}")

