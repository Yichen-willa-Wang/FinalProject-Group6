from transformers import Trainer

class WeightedTrainer(Trainer):
    def compute_loss(self, model, inputs, return_outputs=False, **kwargs):
        """
        Override the compute_loss method to use class weights on the correct device.

        Args:
            model: The model being trained.
            inputs: A dictionary of input tensors.
            return_outputs: Whether to return the model outputs along with the loss.
            **kwargs: Additional arguments that may be passed by the Trainer.

        Returns:
            Loss value and optionally model outputs.
        """
        labels = inputs.pop("labels")  # Extract labels
        outputs = model(**inputs)      # Forward pass
        logits = outputs.logits       # Model predictions

        # Move class weights to the same device as logits
        device = logits.device
        weighted_loss_fn = torch.nn.CrossEntropyLoss(weight=class_weights_tensor.to(device))

        # Compute weighted loss
        loss = weighted_loss_fn(logits, labels)

        # Return outputs if required
        return (loss, outputs) if return_outputs else loss

# Compute Metrics
def compute_metrics(eval_pred):
    logits, labels = eval_pred
    probs = torch.softmax(torch.tensor(logits), dim=-1).numpy()
    predictions = (probs[:, 1] > 0.5).astype(int)

    acc = accuracy_score(labels, predictions)
    precision, recall, f1, _ = precision_recall_fscore_support(labels, predictions, average="binary", zero_division=1)

    return {"accuracy": acc, "precision": precision, "recall": recall, "f1": f1}

