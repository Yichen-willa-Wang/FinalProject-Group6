def swap_words_in_corpus(corpus, target_words, swap_percentage=0.3, stop_words=None):
    """
    Swaps target words across the entire corpus with random words based on a percentage,
    ensuring no new stop words are introduced.

    Args:
        corpus (list of str): List of texts (strings) in the corpus.
        target_words (list): Words to target for swapping.
        swap_percentage (float): Percentage of target words to swap.
        stop_words (set): Set of stop words to ignore in replacements.

    Returns:
        list of str: Modified corpus with words swapped.
    """
    if stop_words is None:
        stop_words = {"the", "is", "a", "an", "and", "or", "in", "of", "to", "on", "at", "with", "for", "by", "it"}

    # Flatten the corpus into a list of words
    all_words = list(chain.from_iterable(text.split() for text in corpus))

    # Filter out stop words from the corpus for replacements
    filtered_corpus_words = [word for word in all_words if word.lower() not in stop_words]

    # Debugging: Check filtered corpus
    print("Filtered Corpus Words (Replacements):", filtered_corpus_words)

    # Count occurrences of target words across the entire corpus
    word_counts = Counter(all_words)
    target_word_counts = {word: word_counts[word] for word in target_words if word in word_counts}

    # Debugging: Check target word counts
    print("Target Word Counts:", target_word_counts)

    # Calculate total swaps needed for each target word
    total_swaps = {word: max(1, int(count * swap_percentage)) if count > 0 else 0 for word, count in target_word_counts.items()}

    # Debugging: Check total swaps per word
    print("Total Swaps Per Word:", total_swaps)

    # Perform swaps in the corpus
    for word, num_swaps in total_swaps.items():
        if num_swaps == 0:
            print(f"No swaps needed for '{word}'.")
            continue  # Skip if no swaps are needed

        # Find all occurrences of the target word across the corpus
        occurrences = [(text_idx, word_idx)
                       for text_idx, text in enumerate(corpus)
                       for word_idx, w in enumerate(text.split()) if w == word]

        # Debugging: Check occurrences of the target word
        print(f"Occurrences of '{word}':", occurrences)

        # Randomly select the occurrences to swap
        if len(occurrences) > 0:
            swap_indices = random.sample(occurrences, min(num_swaps, len(occurrences)))

            # Debugging: Check selected indices for swapping
            print(f"Selected Indices for Swapping '{word}':", swap_indices)

            # Replace the target word with a random non-stop-word from the corpus
            for text_idx, word_idx in swap_indices:
                words = corpus[text_idx].split()
                replacement = random.choice(filtered_corpus_words)
                while replacement in target_words:  # Avoid replacing with another target word
                    replacement = random.choice(filtered_corpus_words)
                words[word_idx] = replacement
                corpus[text_idx] = " ".join(words)
        else:
            print(f"No occurrences of '{word}' found.")

    return corpus

